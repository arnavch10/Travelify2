import React, { useState } from 'react'
import styled from 'styled-components'
import { useRouter } from 'next/router'
import { useStateContext } from '@/context/StateContext'
import { isEmailInUse, register} from '@/backend/Auth'
import Link from 'next/link'
import Navbar from '@/components/Dashboard/Navbar'
const Signup = () => {

  const { user, setUser } = useStateContext()
  const [ email, setEmail ] = useState('')
  const [ password, setPassword ] = useState('')

  const router = useRouter()

  async function validateEmail(){
    const emailRegex = /^[\w.%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}$/;
    if(emailRegex.test(email) == false ){
        return false;
    }
    console.log('so far so good...')
    const emailResponse = await isEmailInUse(email)
    console.log('email response', emailResponse)
    if(emailResponse.length == 0 ){
        return false;
    }

    return true;
}

  async function handleSignup(){
    const isValidEmail = await validateEmail()
    // console.log('isValidEmail', isValidEmail)
    // if(!isValidEmail){ return; }
    
    try{
        await register(email, password, setUser)
        router.push('/dashboard')
    }catch(err){
        console.log('Error Signing Up', err)
    }
  }


  return (
  <>
    <Navbar />
    <Section>
      <FormWrapper>
        <Header>Signup</Header>

        <InputTitle>Email</InputTitle>
        <Input 
          type="email" 
          value={email} 
          onChange={(e) => setEmail(e.target.value)} 
        />

        <InputTitle>Password</InputTitle>
        <Input 
          type="password" 
          value={password} 
          onChange={(e) => setPassword(e.target.value)} 
        />

        <UserAgreementText>
          By signing in, you automatically agree to our 
          <UserAgreementSpan 
            href="/legal/terms-of-use" 
            target="_blank"
          > Terms of Use </UserAgreementSpan> 
            and 
          <UserAgreementSpan 
            href="/legal/privacy-policy" 
            target="_blank"
          > Privacy Policy.</UserAgreementSpan>
        </UserAgreementText>

        <MainButton onClick={handleSignup}>Signup</MainButton>
      </FormWrapper>
    </Section>
  </>
)
}

